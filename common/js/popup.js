function popup() {
    let pop = document.getElementById("popup");
    pop.style.display == "block" ? pop.style.display = "none" : pop.style.display = "block"
}

function seeProfil() {
    let profil = document.getElementById("profil-read-member");
    profil.style.display == "block" ? profil.style.display = "none" : profil.style.display = "block"
}